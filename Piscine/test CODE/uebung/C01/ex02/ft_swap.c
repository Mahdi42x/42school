void ft_swap(int *a, int *b)
{
	int tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

#include <stdio.h>
int main(void)
{
	int z1 = 20;
	int z2 = 10;

	printf("%d, %d\n", z1, z2);
		ft_swap(&z1, &z2);
	printf("%d, %d\n", z1, z2);
}

